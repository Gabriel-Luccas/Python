contador = 0

while contador ==10:
    print(contador)
    contador += 1


#Neste exemplo, o loop while será executado enquanto o valor da variável contador for menor que 10. A cada iteração, o valor do contador será impresso e, em seguida, será incrementado em 1 (contador += 1). O loop continuará até que o valor do contador atinja ou exceda 5.#